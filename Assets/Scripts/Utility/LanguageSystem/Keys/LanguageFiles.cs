namespace Scripts.Utility.LanguageSystem {
    /// <summary>
    /// This enum keys represent all "text groups" for developers
    /// </summary>
    public enum LanguageFiles {
        /// <summary>
        /// Text group: All available menu
        /// </summary>
        MENU,
        /// <summary>
        /// Text group: All available keys for user
        /// </summary>
        NAMES_KEY,
        /// <summary>
        /// Text group: All available language for this game 
        /// </summary>
        AVAILABLE_LANGUAGES,
        /// <summary>
        /// Text group: All graphics texts
        /// </summary>
        GRAPHICS_MENU_TEXTS,
        /// <summary>
        /// Text group: All audio texts
        /// </summary>
        AUDIO_MENU_TEXTS
    }
}